#include <stdio.h>

int main() {
    int A = -2 + 5 * 2;
    int B = 10 / 2 * 3;
    int C = 6 / 2 + 3 * (4 % 2);
    int D = (5 + 2) * 15 % 4;
    int E = 6 + 2 * 2 - 6 / 2;
    int F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    int G = (4 + 3) * 2 - 10 / (2 + 3);

    printf("A = %d\n", A); // 5*2=10 → -2+10=8
    printf("B = %d\n", B); // 10/2=5 → 5*3=15
    printf("C = %d\n", C); // 4%2=0 → 3*0=0 → 6/2=3 → 3+0=3
    printf("D = %d\n", D); // (5+2)=7 → 7*15=105 → 105%4=1
    printf("E = %d\n", E); // 2*2=4, 6/2=3 → 6+4-3=7
    printf("F = %d\n", F); // 3*2=6, 8/4=2, 6%5=1 → 5+6-2+1=10
    printf("G = %d\n", G); // (4+3)=7 → 7*2=14, (2+3)=5 → 10/5=2 → 14-2=12

    return 0;
}